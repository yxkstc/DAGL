public class DADocumentType {
    private Object TypeID;
    private Object TypeNameEnglish;
    private Object TypeNameChinese;
    private Object Createtime;
    private Object State;

    public Object getState() {
        return State;
    }

    public void setState(Object state) {
        State = state;
    }

    public Object getTypeID() {
        return TypeID;
    }

    public void setTypeID(Object typeID) {
        TypeID = typeID;
    }

    public Object getTypeNameEnglish() {
        return TypeNameEnglish;
    }

    public void setTypeNameEnglish(Object typeNameEnglish) {
        TypeNameEnglish = typeNameEnglish;
    }

    public Object getTypeNameChinese() {
        return TypeNameChinese;
    }

    public void setTypeNameChinese(Object typeNameChinese) {
        TypeNameChinese = typeNameChinese;
    }

    public Object getCreatetime() {
        return Createtime;
    }

    public void setCreatetime(Object createtime) {
        Createtime = createtime;
    }


}
